import java.util.*;
 
class Table2{
    public static void main(String args[]){
        Scanner sc = new Scanner(System.in);
        System.out.print("ENTER THE NUMBER :");
        int n = sc.nextInt();
        
        for(int i = 1; i <= 10; i++){
            System.out.println(n*i);
        }
       
    }
}